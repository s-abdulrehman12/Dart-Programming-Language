void main() {
  //if statement only
  var num = 10;
  if (num > 0) {
    print("The Number is Positive :");
  }
  //if else statement only
  var no = -7;
  if (no > 0) {
    print("The number is positive ");
  } else {
    print("The number is negative :");
  }
  //else if ladder statement
  var signal = "green";
  if (signal == "red") {
    print("Stop :");
  } else if (signal == "yellow") {
    print("Ready to go :");
  } else {
    print("Go :)");
  }
}
